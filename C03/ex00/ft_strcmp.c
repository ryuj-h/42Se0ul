#include <unistd.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	is1;
	int	is2;

	while (*s1 != '\0' && (*s1 == *s2))
	{
		s1++;
		s2++;
	}
	is1 = *(unsigned char *)s1;
	is2 = *(unsigned char *)s2;
	return (is1 - is2);
}

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	int	is1;
	int	is2;

	while (*s1 != '\0' && *s2 != '\0' && n > 0)
	{
		if (*s1 != *s2)
			break ;
		s1++;
		s2++;
		n--;
	}
	if (n == 0)
		return (0);
	is1 = *(unsigned char *)s1;
	is2 = *(unsigned char *)s2;
	return (is1 - is2);
}
